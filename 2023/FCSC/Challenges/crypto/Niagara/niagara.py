class Niagara():
    def __init__(self, f = f, g = g):
        self.n = len(f)
        self.f = f
        self.g = g
        self.state = [0] * self.n

    def set_key(self, key):
        for i in range(self.n):
            self.state[i] = (key[i // 8] >> (i % 8)) & 1

    def generate(self):
        res = 0
        k = 0

        if self.g[k] == 1:
            res ^= 1

        for i in range(self.n):
            k += 1
            if self.g[k] == 1:
                res ^= self.state[i]

        for i in range(1, self.n):
            for j in range(i):
                k += 1
                if k < len(self.g) and self.g[k] == 1:
                    res ^= self.state[i] * self.state[j]

        feedback = self.state[self.n-1]
        for i in range(self.n-1, 0, -1):
            self.state[i] = self.state[i-1]
        self.state[0] = 0

        for i in range(self.n):
            self.state[i] ^= feedback * self.f[i]

        return res

    def generate_bytes(self, n):
        res = b''
        for i in range(n):
            res += int.to_bytes(sum([self.generate() * 2**i for i in range(8)]), 1, 'little')
        return res
    
    def encrypt(self, plain):
        keystream = self.generate_bytes(len(plain))
        int_plain = int.from_bytes(plain, 'little')
        int_keystream = int.from_bytes(keystream, 'little')
        int_cipher = int_plain ^ int_keystream
        return int_cipher.to_bytes(len(plain), 'little')

